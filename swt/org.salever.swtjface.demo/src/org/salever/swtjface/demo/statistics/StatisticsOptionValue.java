package org.salever.swtjface.demo.statistics;

/**
 * @author 
 */
public class StatisticsOptionValue {

	private long count;
	
	private double rate;
	
	private StatisticsOption option;

	/**
	 * @return the count
	 */
	public long getCount() {
		return count;
	}

	/**
	 * @param count the count to set
	 */
	public void setCount(long count) {
		this.count = count;
	}

	/**
	 * @return the rate
	 */
	public double getRate() {
		return rate;
	}

	/**
	 * @param rate the rate to set
	 */
	public void setRate(double rate) {
		this.rate = rate;
	}

	/**
	 * @return the option
	 */
	public StatisticsOption getOption() {
		return option;
	}

	/**
	 * @param option the option to set
	 */
	public void setOption(StatisticsOption option) {
		this.option = option;
	}
	
	
}
