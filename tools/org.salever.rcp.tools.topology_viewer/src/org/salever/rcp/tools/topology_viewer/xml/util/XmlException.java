package org.salever.rcp.tools.topology_viewer.xml.util;

/**
 * The Class XmlException.
 */
public class XmlException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -9044930243001212943L;

	/**
	 * The Constructor.
	 */
	public XmlException() {
		super();
	}

	/**
	 * The Constructor.
	 * 
	 * @param message
	 *            the message
	 */
	public XmlException(String message) {
		super(message);
	}

	/**
	 * The Constructor.
	 * 
	 * @param e
	 *            the e
	 */
	public XmlException(Throwable e) {
		super(e);
	}

}
